L = [5, 7, 2, 9, 4, 1, 3]

# Quantidade de elementos na lista
quantidade_elementos = len(L)
print("Quantidade de elementos na lista:", quantidade_elementos)

# Maior valor presente na lista
maior_valor = max(L)
print("Maior valor presente na lista:", maior_valor)

# Menor valor presente na lista
menor_valor = min(L)
print("Menor valor presente na lista:", menor_valor)

# Soma de todos os elementos da lista
soma_elementos = sum(L)
print("Soma de todos os elementos da lista:", soma_elementos)

# Lista em ordem crescente
lista_crescente = sorted(L)
print("Lista em ordem crescente:", lista_crescente)

# Lista em ordem decrescente
lista_decrescente = sorted(L, reverse=True)
print("Lista em ordem decrescente:", lista_decrescente)